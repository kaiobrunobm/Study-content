// Objetos globais

let person = Object(); // Esse objeto global vai armazenar um objeto vazio ou com elementos informados dentro da variável person
console.log(person);

person = String(22); // Esse vai tenter tranformar qualquer dado informado pra string
console.log(person);

person = Array(); // Esse vai armazenar uma array vazia ou com dados informados dentro da variável person
console.log(person);

person = Number(true); // Esse vai tenter tranformar qualquer dado informado pra um dado de tipo number
console.log(person);

person = Date(); // Esse vai armazenar a data do dia atual que o javascript vai pushar do seu cumputado
console.log(person);

person = Math.pow(); //O math vai fazer alguma operação matemática que vem pré pronta no javascript como lagaritimo ou raiz.
console.log(person);
